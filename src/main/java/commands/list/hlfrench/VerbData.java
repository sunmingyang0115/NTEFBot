package commands.list.hlfrench;

public class VerbData {
    String tense;
    String preconj;
    String pronoun;
    String verb;
    String infinitive;
    public VerbData(String _tense, String _preconj, String _pronun, String _verb, String _infinitive) {
        tense = _tense;
        preconj = _preconj;
        pronoun = _pronun;
        verb = _verb;
        infinitive = _infinitive;
    }
}
